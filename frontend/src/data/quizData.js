const generateLevels = (topic) => {
  const levels = {};
  for (let i = 1; i <= 20; i++) {
    let pool;
    if (topic === 'Interview') {
      if (i <= 4) pool = interviewBeginner;
      else if (i <= 8) pool = interviewIntermediate;
      else if (i <= 14) pool = interviewAdvanced;
      else pool = interviewExpert;
    } else if (topic === 'Presentation') {
      pool = presentation;
    } else if (topic === 'Viva') {
      pool = viva;
    } else if (topic === 'Group Discussion') {
      pool = groupDiscussion;
    } else if (topic === 'Meeting') {
      pool = meeting;
    } else if (topic === 'First Date') {
      pool = firstDate;
    }
    levels[`Level ${i}`] = [...pool];
  }
  return levels;
};

